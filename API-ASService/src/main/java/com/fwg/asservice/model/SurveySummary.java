package com.fwg.asservice.model;

public class SurveySummary {

	private Integer total;
	private Integer survey;
	private String headerTypeCode;
	
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	public Integer getSurvey() {
		return survey;
	}
	public void setSurvey(Integer survey) {
		this.survey = survey;
	}
	public String getHeaderTypeCode() {
		return headerTypeCode;
	}
	public void setHeaderTypeCode(String headerTypeCode) {
		this.headerTypeCode = headerTypeCode;
	}
}
